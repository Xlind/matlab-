f=imread('D:\photo\me111.jpg');
subplot(3,3,1),imshow(f);
whos f;%其实是个二值图像
title('原图'); 
f=im2double(f);%不再是二值图像了
h=fspecial('gaussian',15,9);
g=imfilter(f,h,'replicate');
g=im2bw(g,0.7);%其中的graythresh()函数为自动阈值，处理后g为二值图像了
subplot(3,3,2),imshow(g),title('经阈值处理后');
b=boundaries(g);
b=b{1};
bim=bound2im(b,313,423);%313,423为图片的尺寸
subplot(3,3,3),imshow(bim),title('提取的边界图');
z=frdescp(b);%对边界坐标b进行傅里叶变换系数(有多少个点就有多少个系数)，将b的坐标点看成是复平面中的某个复数
z14=ifrdescp(z,546);%用50%的描述子进行逆变换
z546im=bound2im(z14,313,423);
subplot(3,3,4),imshow(z546im),title('546个描述子恢复后');
z110=ifrdescp(z,110);%用50%的描述子进行逆变换
z110im=bound2im(z110,313,423);
subplot(3,3,5),imshow(z110im),title('110个描述子恢复后');
